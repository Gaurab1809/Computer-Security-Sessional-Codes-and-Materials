    for(int i=0;i<4;i++) temp[i]=w3[i];
    RotWord(temp); SubWord(temp); temp[0]^=Rcon[1];
    for(int i=0;i<4;i++) w4[i]=w0[i]^temp[i];
    for(int i=0;i<4;i++) w5[i]=w1[i]^w4[i];
